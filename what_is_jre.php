<br><br>

<p class="intro_123" align="center">
The Java Runtime Environment (JRE), also known as Java Runtime, is the part of the Java Development Kit (JDK) that contains and orchestrates the set of tools and minimum requirements for executing a Java application. The JDK, along with the Java Virtual Machine (JVM) and the JRE, can be used by developers to program and run Java applications.



	</p>


<br>
<br>

<p class="intro_123" align="center">
	
It includes a large number of java development tools and java classes and methods.
The Java development tools are part of the system known as Java Development Kit (JDK) and the classes and the methods in Java are a part of the Java Standard Library (JSL), also known as the Application Programming Interface (API).JRE is an acronym for Java Runtime Environment. It is used to provide runtime environment to the Java code.

The Java Runtime Environment provides the minimum requirements for executing a Java application; it consists of the Java Virtual Machine (JVM), core classes, and supporting files.

Java Runtime Environment contains JVM, class libraries, and other supporting files. It does not contain any development tools such as compiler, debugger, etc. Actually JVM runs the program, and it uses the class libraries, and other supporting files provided in JRE. If you want to run any java program, you need to have JRE installed in the system.
</p>
