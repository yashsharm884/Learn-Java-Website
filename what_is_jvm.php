<br><br>
	<p class="intro_123" align="center">
The JVM performs following operation:
</p><br><br>
<p class="intro_123" align="center">Loads code</p>
<p class="intro_123" align="center">Verifies code</p>
<p class="intro_123" align="center">Executes code</p>
<p class="intro_123" align="center">Provides runtime environment</p>
